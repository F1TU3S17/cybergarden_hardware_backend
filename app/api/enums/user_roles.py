"""Перечисление ролей пользователей.

Этот модуль определяет роли пользователей для системы
авторизации и управления доступом.

Enums:
    UserRole: Роли пользователей в системе
"""

from enum import Enum


class UserRole(str, Enum):
    """Роли пользователей в системе.
    
    Определяет уровни доступа и полномочия пользователей
    в системе мониторинга IoT устройств.
    
    Attributes:
        ADMIN: Администратор с полными правами
               - Управление пользователями
               - Управление устройствами
               - Полный доступ ко всем функциям
        USER: Обычный работник (оператор)
              - Просмотр данных устройств
              - Работа с оповещениями
              - Ограниченные права на изменение
        
    Example:
        >>> user = User(
        ...     username="admin",
        ...     role=UserRole.ADMIN
        ... )
        >>> if user.role == UserRole.ADMIN:
        ...     print("Полный доступ")
        
    Note:
        Роли используются для реализации Role-Based Access Control (RBAC).
    """
    ADMIN = "admin"
    USER = "worker"  # Обычный работник/оператор